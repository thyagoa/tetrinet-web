// Cooldown tracking
const _narratorCooldowns = {};

const narrator = {
  pick(key) {
    const arr = NARRATOR_PHRASES[key];
    if (!arr || arr.length === 0) return null;
    return arr[Math.floor(Math.random() * arr.length)];
  },

  bomb(special, context) {
    // Switch uses its own key scheme to share ally/enemy pool
    if (special === 's') {
      return this.pick(context === 'self' ? 'switch.self' : 'switch.other');
    }
    return this.pick(`${special}.${context}`);
  },

  event(type, cooldownMs = 0) {
    if (cooldownMs > 0) {
      const now = Date.now();
      if (_narratorCooldowns[type] && now - _narratorCooldowns[type] < cooldownMs) return null;
      _narratorCooldowns[type] = now;
    }
    return this.pick(type);
  },
};
